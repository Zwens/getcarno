// Generate TypeScript file
const byStroke = new Map();
for (const [char, val] of existing) {
  if (!byStroke.has(val)) byStroke.set(val, []);
  byStroke.get(val).push(char);
}

let body = '';
const strokes = [...byStroke.keys()].sort((a, b) => a - b);
for (const stroke of strokes) {
  const chars = byStroke.get(stroke);
  // Split into lines of ~8 per line
  const chunkSize = 8;
  body += `\n  // ── ${stroke} 画 ──\n`;
  for (let i = 0; i < chars.length; i += chunkSize) {
    const chunk = chars.slice(i, i + chunkSize);
    body += '  ' + chunk.map(c => `'${c}': ${stroke}`).join(', ') + ',\n';
  }
}

const output = `// src/data/kangxiBihua.ts

/**
 * 康熙字典笔画表（常用汉字）
 * 注意：康熙笔画与现代简体笔画有差异，本表以康熙字典原文笔画为准
 * 用于姓名学五格数理分析
 *
 * 主要简繁差异示例（简体 → 康熙笔画）：
 *   万(3)→15  云(4)→12  龙(5)→16  华(6)→14  国(8)→11
 *   学(8)→16  陈(7)→16  杨(7)→13  叶(5)→15  涛(10)→18
 *   宝(8)→20  艺(4)→21  丰(4)→18  伟(6)→11  荣(9)→14
 */
export const KANGXI_BIHUA: Record<string, number> = {
${body}}

/**
 * 获取单个汉字的康熙笔画数
 * @param char 汉字
 * @returns 康熙笔画数，未找到返回默认值10
 */
export function getBihua(char: string): number {
  if (KANGXI_BIHUA[char] !== undefined) {
    return KANGXI_BIHUA[char]
  }
  console.warn(\`未找到"\${char}"的康熙笔画数据，使用默认值\`)
  return 10
}

/**
 * 获取名字每个字的康熙笔画数数组
 * @param name 姓名字符串
 * @returns 每个字对应的康熙笔画数数组
 */
export function getNameBihua(name: string): number[] {
  return Array.from(name).map(getBihua)
}
`;

fs.writeFileSync(path.join(__dirname, '../src/data/kangxiBihua.ts'), output, 'utf8');
